# Array reversing

arr=[10,20,30,40]
x=arr[::-1]
print(x)

# Delete an element from aa array using remove (when we want to remove an element from array by without indexing we use remove)

arr=[10,20,30,40]
x=arr.remove(10)
print(arr)

# Delete an element from aa array using pop (when we want to remove an element from array by using indexing we use pop)

arr=[10,20,30,40]
x=arr.pop(2)
print(arr)



